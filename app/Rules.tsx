// 1. THÔNG TIN CHUNG
// 1.1 Tham Gia
// Số lượng tham gia triển lãm có hạn và dựa trên đăng ký trước.
// Người tham gia cần tham khảo lịch trình chi tiết của triển lãm để biết thời gian và ngày cụ thể.
// Việc đăng ký sẽ đóng lại khi đạt số lượng tối đa cho mỗi hạng mục.
// 1.2 Đăng Ký
// Tất cả cá Koi tham gia phải được đăng ký trước sự kiện thông qua hệ thống đăng ký chính thức.
// Mỗi người tham gia phải cung cấp thông tin chính xác về cá Koi của mình bao gồm giống, kích thước và tuổi.
// Phí đăng ký phải được thanh toán đầy đủ trước ngày triển lãm.
// 2. CẤU TRÚC CUỘC THI
// 2.1 Các Vòng Thi
// Mỗi hạng mục trong cuộc thi bao gồm ba giai đoạn đánh giá riêng biệt:
// Vòng Sơ Khảo (1 vòng)
// Vòng Đánh Giá Chính (2 vòng)
// Vòng Chung Kết (1 vòng)
// 2.2 Vòng Sơ Khảo
// Tất cả cá Koi đã đăng ký sẽ được đánh giá ban đầu.
// Chấm điểm dựa trên tiêu chí Đạt/Không Đạt.
// Giám khảo sẽ xác minh xem cá Koi có thuộc đúng hạng mục đã đăng ký hay không.
// Chỉ những cá Koi đạt yêu cầu ở vòng này mới được tiếp tục vào Vòng Đánh Giá Chính.
// Cá Koi bị loại phải được đưa ra khỏi khu vực thi đấu ngay lập tức.
// 2.3 Vòng Đánh Giá Chính
// Bao gồm hai vòng chấm điểm riêng biệt, cả hai vòng đều áp dụng cùng một bộ tiêu chí đánh giá.
// Tiêu chí chấm điểm chỉ phụ thuộc vào loại Vòng Đánh Giá Chính, không thay đổi giữa vòng 1 và vòng 2.
// Cá sẽ được đánh giá dựa trên các tiêu chí cụ thể cho từng hạng mục.
// Tiêu chí có thể bao gồm hình dáng cơ thể, kiểu mẫu màu sắc, chất lượng da và sức khỏe tổng thể.
// Điểm số sẽ được trao theo hệ thống chấm điểm.
// 2.4 Vòng Chung Kết
// Những cá Koi có điểm cao nhất từ Vòng Đánh Giá Chính sẽ thi đấu.
// Đánh giá dựa trên các tiêu chí giống như Vòng Đánh Giá Chính.
// Điểm số cuối cùng xác định người chiến thắng và thứ hạng trong mỗi hạng mục.
// Quyết định của giám khảo là cuối cùng và không thể tranh cãi.
// 3. TIÊU CHÍ CHẤM ĐIỂM
// 3.1 Tiêu Chí Đặc Thù Theo Hạng Mục
// Mỗi hạng mục có tiêu chí chấm điểm riêng phù hợp với đặc điểm của giống cá.
// Chi tiết về tiêu chí đặc thù theo hạng mục sẽ được cung cấp trong tài liệu triển lãm.
// 4. TRÁCH NHIỆM CỦA NGƯỜI THAM GIA
// 4.1 Xử Lý và Chăm Sóc
// Người tham gia chịu trách nhiệm về việc xử lý đúng cách cá Koi của mình.
// Tất cả cá Koi phải được thích nghi đầy đủ trước khi được đặt vào bể triển lãm.
// Bất kỳ cá Koi nào có dấu hiệu bệnh tật hoặc căng thẳng sẽ bị loại khỏi cuộc thi.
// 4.2 Quản Lý Thời Gian
// Người tham gia phải tuân thủ lịch trình đã công bố cho tất cả các vòng.
// Đến muộn có thể dẫn đến việc bị loại khỏi cuộc thi.
// Cá Koi phải sẵn sàng để chấm điểm trong khoảng thời gian quy định cho mỗi vòng.
// 5. GIẢI THƯỞNG VÀ DANH HIỆU
// 5.1 Giải Thưởng Theo Hạng Mục
// Giải thưởng sẽ được trao cho vị trí nhất, nhì và ba trong mỗi hạng mục.
// Có thể có sự công nhận đặc biệt cho các cá thể xuất sắc.
// 6. THÔNG TIN BỔ SUNG
// 6.1 Thay Đổi Lịch Trình
// Ban tổ chức có quyền điều chỉnh lịch trình nếu cần thiết.
// Tất cả các thay đổi sẽ được thông báo kịp thời đến những người tham gia đã đăng ký.
// 6.2 Tranh Chấp và Khiếu Nại
// Bất kỳ mối quan ngại nào về việc chấm điểm phải được gửi bằng văn bản đến ban tổ chức.
// Quyết định của ban tổ chức về các tranh chấp là cuối cùng.
// 7. QUY TẮC ỨNG XỬ
// 7.1 Hành Vi Đạo Đức
// Tất cả người tham gia phải thể hiện sự tôn trọng đối với những người tham gia khác, giám khảo và nhân viên.
// Hành vi thiếu tinh thần thể thao có thể dẫn đến việc bị loại và đưa ra khỏi sự kiện.
// 7.2 Chụp Ảnh và Truyền Thông
// Chụp ảnh chỉ được phép ở những khu vực được chỉ định.
// Tất cả đại diện truyền thông phải có sự cho phép trước.
